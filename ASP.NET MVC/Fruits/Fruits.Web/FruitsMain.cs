using System.IO;
using Microsoft.AspNetCore.Hosting;

namespace Fruits.Web
{
    public class FruitsMain
    {
        public static void Main()
        {
            var host = new WebHostBuilder()
                .UseKestrel()
                .UseContentRoot(Directory.GetCurrentDirectory())
                .UseIISIntegration()
                .UseStartup<Startup>()
                .Build();

            host.Run();
        }
    }
}
